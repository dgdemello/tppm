    private void btAlterarEmpregadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarEmpregadoActionPerformed
        String cpf = cpfInput.getText();
        try{
            Empregado empregado = empregadoController.procurarEmpregado(cpf);
            if(empregado != null){
                fecharAlterarEmpregadoBox();
                abrirBoxAlterarEmpregadoForm(empregado);
            }
            else{
                Utils.exibeErro(this, "NÃ£o existe nenhum empregado com esse cpf cadastrado!");
            }
        }
        catch(EmpregadoDAOException e){
            Utils.exibeErro(this, "Erro ao procurar o empregado!");
        }
        catch(ValidacaoEmpregadoException e){
            Utils.exibeErro(this, e.getMessage());
        }
    }//GEN-LAST:event_btAlterarEmpregadoActionPerformed
    
    public void fecharAlterarEmpregadoBox() {
        dispose();
    }
    
    private void abrirBoxAlterarEmpregadoForm(Empregado empregado){
        JFrame mainFrame = TppmApp.getApplication().getMainFrame();
        alterarEmpregadoFormBox = new TppmAlterarEmpregadoFormBox(empregado);
        alterarEmpregadoFormBox.setLocationRelativeTo(mainFrame);
        TppmApp.getApplication().show(alterarEmpregadoFormBox);
    }
    
